package day2;

public class FourArithmetic
{
    public static void main(String[] args)
    {
        int a = 12;
        int b = 2;

        int result = a + b;
        System.out.println("a + b = " + result);

        result = a - b;
        System.out.println("a - b = " + result);

        result = a * b;
        System.out.println("a * b = " + result);

        result = a / b;
        System.out.println("a / b = " + result);
    }
}
