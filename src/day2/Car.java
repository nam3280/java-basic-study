package day2;

public class Car
{
    String model;
    long distance;
    int price;
    String company;
    char type;
    boolean auto;
    int year;
    float gasmi;

    public String getModel()
    {
        return this.model;
    }

    public long getDistance()
    {
        return this.distance;
    }

    public int getPrice()
    {
        return this.price;
    }

    public void setModel(String model)
    {
        this.model = model;
    }
}
